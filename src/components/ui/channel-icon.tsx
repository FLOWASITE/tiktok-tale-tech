interface ChannelIconProps {
  channel: Channel;
  className?: string;
  size?: number;
}

export function ChannelIcon({ channel, className = '', size = 16 }: ChannelIconProps) {
  const style = { width: size, height: size };

  // Fallback for channels without custom SVG
  if (channel === 'website') {
    return <Globe className={className} style={style} />;
  }
  if (channel === 'email') {
    return <Mail className={className} style={style} />;
  }

  const SvgIcon = svgIcons[channel];
  if (SvgIcon) {
    return <span style={style} className={`inline-block ${className}`}><SvgIcon className="w-full h-full" /></span>;
  }

  return <Globe className={className} style={style} />;
}

